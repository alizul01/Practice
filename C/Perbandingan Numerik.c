#include <stdio.h>

int main()
{
  int x = 5;
  int y = 10;

  puts("Nilai x adalah 5");
  puts("Nilai y adalah 10\n");
  printf("Hasil x < y : %d \n", x<y);
  printf("Hasil x > y : %d \n", x > y);
  printf("Hasil x <= y : %d \n", x <= y);
  printf("Hasil x => y : %d \n", x >= y);
  printf("Hasil x = y : %d \n", x==y);
  printf("Hasil x != y : %d\n", x != y);
  return 0;
}