public class nestjika {
    public static void main(String[] args) {
        
        // nested if

        int a = 10;
        int b = 10;

        System.out.println("Ini adalah awal dari program");

        if (a > b) {
            System.out.println("a > b");
        }
        else if (a < b) {
            System.out.println("a < b");
        }
        else {
            System.out.println("a = b");
        }
    }
}
